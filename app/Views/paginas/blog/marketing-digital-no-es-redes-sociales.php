									El  marketing digital y las redes sociales son dos términos que a menudo se utilizan bastantes, pero en realidad son dos cosas totalmente diferentes. Aunque ambos son importantes para el éxito de cualquier empresa en el mundo digital, es importante entender las diferencias entre ellos. 
